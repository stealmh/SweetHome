    /// - ÏÉÅÎã® Ï†ïÎ≥¥ ÏÑπÏÖò ÏÑ§Ï†ï
    func setupTopInfoSection(_ detail: DetailEstate) {
        let topInfoItem = Item.topInfo(detail)
