        output.brokerChatButtonTappedResult
            .drive(onNext: { [weak self] _ in
                // TODO: Ï±ÑÌåÖ ÌôîÎ©¥ÏúºÎ°ú Ïù¥Îèô
                print("Ï±ÑÌåÖ ÌôîÎ©¥ Ïù¥Îèô")
            })
            .disposed(by: disposeBag)
        
        /// - Ïú†ÏÇ¨Ìïú Îß§Î¨º ÏÖÄ ÎàåÎ†ÄÏùÑ Îïå
